let currentLocale = localStorage.getItem('pedrra_locale') || navigator.language?.slice(0, 2) || 'en';
if (!translations[currentLocale]) currentLocale = 'en';

export function t(key, params = {}) {
  let str = translations[currentLocale]?.[key] || translations.en[key] || key;
  for (const [k, v] of Object.entries(params)) {
    str = str.replace(`{${k}}`, v);
  }
  return str;
}

export function setLocale(locale) {
  if (translations[locale]) {
    currentLocale = locale;
    localStorage.setItem('pedrra_locale', locale);
  }
}

export function getLocale() { return currentLocale; }
export function getAvailableLocales() {
  return Object.keys(translations).map(code => ({
    code,
    name: { en: 'English', es: 'Español', fr: 'Français' }[code] || code,
  }));
}
